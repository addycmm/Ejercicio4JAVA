    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
        URL url = new URL("http://localhost/examenA/Ejercicio4/index.php");
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        conn.setRequestMethod("GET");
        BufferedReader rd = new BufferedReader(new InputStreamReader(conn.getInputStream()));
        StringBuilder response = new StringBuilder();
        String line;
        while ((line = rd.readLine()) != null) {
            response.append(line);
        }
        rd.close();
        System.out.println("Datos recibidos en Java: " + response.toString());

       
        ObjectMapper mapper = new ObjectMapper();
        JsonNode jsonNode = mapper.readTree(response.toString());

      
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        
        for (JsonNode node : jsonNode) {
            String persona_id = node.get("id").asText();
            String nombre = node.get("nombre").asText();
            String ap_paterno = node.get("apellido").asText();
            String ap_materno = node.get("ci").asText();
            String correo = node.get("correo").asText();
            String tipo = node.get("contrasenia").asText();
            String departamento = node.get("departamento").asText();
          
            model.addRow(new Object[]{persona_id, nombre, ap_paterno, ap_materno, correo, tipo, departamento});
        }
    } 
        catch (Exception e) {}
    }//GEN-LAST:event_jButton1ActionPerformed

